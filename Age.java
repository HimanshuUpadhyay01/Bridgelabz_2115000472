public class Age {
    public static void main(String[] args) {
        // Initialize the year of birth
        int yearOfBirth = 2000;

        // Initialize the current year
        int yearNow = 2024;

        // Calculate Harry's age
        int harrysAge = yearNow - yearOfBirth;

        // Print Harry's age in the current year
        System.out.println("Harry's age in 2024 is " + harrysAge);
    }

}